
public class Componente {
	private String id;
	private String nome;
	private String preco;
	
	public String getID() {
		return id;
	}
	public void setID(String valor) {
		this.id = valor;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getPreco() {
		return preco;
	}
	public void setPreco(String preco) {
		this.preco = preco;
	}
}
